<h2 id="info">
    Hvad er KFUM spejderne?
</h2>
<p>
    KFUM står for “Kristen Forening for Unge Mænd”. Det er en spejderbevægelse, der blev startet tilbage i år 1910. Til en start var det kun drenge, der kunne være spejdere, men i 1982 blev korpset for alle køn.

    KFUM Spejderne i Otterup her på Nordfyn, kalder vi også for Slettefolket.

    I vores spejdergruppe dyrker vi udendørslivet via leg og læring. Dog med det twist at KFUM er en kristen organisation. Vi dyrker dog ikke religionen så meget som i gamle dage, men vi starter og slutter altid vores spejdermøder med en kristen spejdersang. Det kan fx. også dukke op i form af sang til vores flaghejsninger og bordbønner før vi spiser på vores spejderture. 

    Her hos Otterup KFUM spejderne deler vi vores medlemmer op i grupper afhængig af deres alder:
    Familiespejdere - 3-6 år og deres forældre.
    Bæverspejdere - 6-9 år
    Ulvespejdere - 9-12 år
    Tropspejdere - 12-18 år.
    Når du bliver 18 år vil du også blive spejderleder.

</p>

